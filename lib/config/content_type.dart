/// Enum representing different types of Islamic content
enum ContentType {
  /// A chapter (Surah) from the Quran
  surah,

  /// A supplication or prayer (Dua)
  dua,

  /// A verse or verses (Ayat) from the Quran
  ayat,

  /// A specific type of prayer (Darood)
  darood,

  /// Other Islamic content
  other,
}
